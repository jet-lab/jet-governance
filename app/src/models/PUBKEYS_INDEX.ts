export const getFirstTwoHundredPubkeys = async (communityMint: PublicKey | undefined) => {
  if (!communityMint) {
    return;
  }

  const pubkeysIndex: string[] = [];
  for (let i = 0; i < 200; i++) {
    let proposalIndexBuffer = Buffer.alloc(4);
    proposalIndexBuffer.writeInt32LE(i, 0);
    const [proposalAddress] = await PublicKey.findProgramAddress(
      [
        Buffer.from(GOVERNANCE_PROGRAM_SEED),
        JET_GOVERNANCE.toBuffer(),
        communityMint.toBuffer(),
        proposalIndexBuffer
      ],
      StakeClient.GOVERNANCE_PROGRAM_ID
    );
    pubkeysIndex.push(proposalAddress.toString());
  }

  const pubkeysIndexArr = Object.assign(
    {},
    ...pubkeysIndex.map(key => ({
      [key]: pubkeysIndex.indexOf(key) + 1
    }))
  );
  return console.log(JSON.stringify(pubkeysIndexArr, null, 2));
};

